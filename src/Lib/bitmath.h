#ifndef _BITMATH_H_
#define _BITMATH_H_

//*******************************************************************************************************************************
//
// .d8888. d88888b  .o88b. d888888b d888888b  .d88b.  d8b   db      db 
// 88'  YP 88'     d8P  Y8 `~~88~~'   `88'   .8P  Y8. 888o  88     o88 
// `8bo.   88ooooo 8P         88       88    88    88 88V8o 88      88 
//   `Y8b. 88~~~~~ 8b         88       88    88    88 88 V8o88      88 
// db   8D 88.     Y8b  d8    88      .88.   `8b  d8' 88  V888      88 
// `8888Y' Y88888P  `Y88P'    YP    Y888888P  `Y88P'  VP   V8P      VP 
//
//*******************************************************************************************************************************

#define SET_BIT(REGISTER, BIT_NUM)      (REGISTER |= (1<<BIT_NUM))
#define CLEAR_BIT(REGISTER, BIT_NUM)    (REGISTER &= ~(1<<BIT_NUM))
#define TOGGLE_BIT(REGISTER, BIT_NUM)   (REGISTER ^= (1<<BIT_NUM))
#define GET_BIT(REGISTER, BIT_NUM)      ((REGISTER >> BIT_NUM) & 1)

#endif  //_BITMATH_H_

